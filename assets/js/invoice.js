/* ============================================================
   PAK VISION INVENTORY PRO — invoice.js
   Builds and prints a professional invoice for a saved sale.
   ============================================================ */

const Invoice = {
  printSale(saleId) {
    const sale = dbFind('sales', saleId);
    if (!sale) { Toast.show('Invoice not found', 'error'); return; }
    const settings = getSettings();
    const win = window.open('', '_blank', 'width=850,height=900');
    if (!win) { Toast.show('Please allow popups to print invoices', 'warning'); return; }

    const rows = sale.items.map((it, i) => `
      <tr>
        <td>${i + 1}</td>
        <td>${escapeHtml(it.name)}</td>
        <td class="c">${fmtNum(it.qty)} ${escapeHtml(it.unit || '')}</td>
        <td class="r">${fmtMoney(it.price)}</td>
        <td class="r">${fmtMoney(it.price * it.qty)}</td>
      </tr>`).join('');

    const html = `
      <!doctype html><html><head><meta charset="utf-8"><title>Invoice ${escapeHtml(sale.invoiceNo)}</title>
      <style>
        * { box-sizing: border-box; }
        body { font-family: 'Segoe UI', Arial, sans-serif; color:#1a1d23; margin:0; padding:32px; }
        .invoice-head { display:flex; justify-content:space-between; align-items:flex-start; border-bottom: 3px dashed #C98A2E; padding-bottom:18px; margin-bottom:18px; }
        .shop-logo { max-height:64px; margin-bottom:8px; }
        .shop-name { font-size:24px; font-weight:800; margin:0; letter-spacing:0.3px; }
        .shop-meta { font-size:12.5px; color:#555; line-height:1.5; margin-top:4px; }
        .invoice-title { text-align:right; }
        .invoice-title h2 { margin:0; font-size:20px; letter-spacing:1px; color:#C98A2E; }
        .invoice-meta { font-size:12.5px; color:#444; margin-top:6px; line-height:1.6; }
        .bill-to { margin-bottom:18px; font-size:13px; }
        .bill-to strong { display:block; font-size:14px; margin-bottom:2px; }
        table { width:100%; border-collapse:collapse; margin-bottom:18px; }
        th { background:#12233b; color:#fff; padding:8px 10px; font-size:12px; text-align:left; }
        td { padding:8px 10px; font-size:13px; border-bottom:1px solid #e5e5e5; }
        .c { text-align:center; } .r { text-align:right; }
        .totals { width:280px; margin-left:auto; font-size:13.5px; }
        .totals div { display:flex; justify-content:space-between; padding:5px 0; }
        .totals .grand { font-weight:800; font-size:16px; border-top:2px solid #12233b; margin-top:6px; padding-top:8px; }
        .footer { margin-top:28px; text-align:center; font-size:12.5px; color:#666; border-top:1px dashed #C98A2E; padding-top:14px; }
        .stamp { display:inline-block; margin-top:10px; font-size:11px; color:#999; }
        @media print { body { padding:10px; } }
      </style></head>
      <body>
        <div class="invoice-head">
          <div>
            ${settings.logo ? `<img class="shop-logo" src="${settings.logo}">` : ''}
            <p class="shop-name">${escapeHtml(settings.shopName)}</p>
            <div class="shop-meta">${escapeHtml(settings.address)}<br>${escapeHtml(settings.phone)}</div>
          </div>
          <div class="invoice-title">
            <h2>SALES INVOICE</h2>
            <div class="invoice-meta">
              Invoice #: <strong>${escapeHtml(sale.invoiceNo)}</strong><br>
              Date: ${fmtDateTime(sale.date)}<br>
              Payment: ${escapeHtml(sale.paymentMethod)}
            </div>
          </div>
        </div>

        <div class="bill-to">
          <strong>Billed To:</strong>
          ${escapeHtml(sale.customerName || 'Walk-in Customer')}
        </div>

        <table>
          <thead><tr><th>#</th><th>Product</th><th class="c">Qty</th><th class="r">Price</th><th class="r">Subtotal</th></tr></thead>
          <tbody>${rows}</tbody>
        </table>

        <div class="totals">
          <div><span>Subtotal</span><span>${fmtMoney(sale.subtotal)}</span></div>
          <div><span>Discount</span><span>- ${fmtMoney(sale.discount)}</span></div>
          <div class="grand"><span>Grand Total</span><span>${fmtMoney(sale.grandTotal)}</span></div>
        </div>

        <div class="footer">
          ${escapeHtml(settings.invoiceFooter)}
          <div class="stamp">Generated by Pak Vision Inventory Pro &middot; Offline System</div>
        </div>

        <script>window.onload = () => { window.print(); };</script>
      </body></html>
    `;
    win.document.write(html);
    win.document.close();
  },

  printPurchase(purchaseId) {
    const purchase = dbFind('purchases', purchaseId);
    if (!purchase) return;
    const settings = getSettings();
    const win = window.open('', '_blank', 'width=850,height=900');
    if (!win) { Toast.show('Please allow popups to print', 'warning'); return; }
    const rows = purchase.items.map((it, i) => `
      <tr><td>${i + 1}</td><td>${escapeHtml(it.name)}</td><td class="c">${fmtNum(it.qty)}</td><td class="r">${fmtMoney(it.cost)}</td><td class="r">${fmtMoney(it.cost * it.qty)}</td></tr>`).join('');
    win.document.write(`
      <!doctype html><html><head><meta charset="utf-8"><title>Purchase ${escapeHtml(purchase.purchaseNo)}</title>
      <style>body{font-family:Arial,sans-serif;padding:32px;color:#1a1d23;} table{width:100%;border-collapse:collapse;} th{background:#12233b;color:#fff;padding:8px;text-align:left;font-size:12px;} td{padding:8px;border-bottom:1px solid #eee;font-size:13px;} .c{text-align:center;} .r{text-align:right;}</style>
      </head><body>
      <h2>${escapeHtml(settings.shopName)} — Purchase Order</h2>
      <p>Purchase #: <strong>${escapeHtml(purchase.purchaseNo)}</strong> | Date: ${fmtDate(purchase.date)} | Supplier: ${escapeHtml(purchase.supplierName)}</p>
      <table><thead><tr><th>#</th><th>Product</th><th class="c">Qty</th><th class="r">Cost</th><th class="r">Subtotal</th></tr></thead><tbody>${rows}</tbody></table>
      <h3 style="text-align:right;">Total: ${fmtMoney(purchase.totalCost)}</h3>
      <script>window.onload=()=>window.print();</script>
      </body></html>
    `);
    win.document.close();
  }
};
